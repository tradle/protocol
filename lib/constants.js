const REQUIRED_NON_IDENTITY_PROPS = REQUIRED_IDENTITY_PROPS.concat(AUTHOR)

module.exports = {
  HEADER_PROPS,
  REQUIRED_IDENTITY_PROPS,
  REQUIRED_NON_IDENTITY_PROPS
}
